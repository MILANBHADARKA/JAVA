public class B {
    public static void main(String[] args) {
        int x = 100;
        System.out.printf("Printitng simple int : x = %10d\n", x);

        float pi = 3.14f;
        System.out.printf("Printing float : pi = %.2f\n", pi);

        float n = 5.2f;
        System.out.printf("Printing float with precision : n = %.4f\n", n);

        float p1 = 2324435.3f;
        System.out.printf("Printing large float : p1 = %20.4f\n", p1);
    }
}
                                                       