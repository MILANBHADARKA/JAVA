public class Premitive_2 {
    public static void main(String[] args) {
        //primitive data types
        int x = 5;
        byte e = 5;
        float y = 5.5f;        //by default any decimal number is double in java so we have to add 'f' at the end of the number to make it float otherwise it will give error
        double z = 5.5;
        char a = 'A';
        boolean b = true;
        long c = 5555555555l;  // by default any number is int in java so we have to add 'l' at the end of the number to make it long otherwise it will give error because it is vary big number
        short d = 5;

        System.out.println("int: " + x);
        System.out.println("byte: " + e);
        System.out.println("float: " + y);
        System.out.println("double: " + z);
        System.out.println("char: " + a);
        System.out.println("boolean: " + b);
        System.out.println("long: " + c);
        System.out.println("short: " + d);

    }
}
 